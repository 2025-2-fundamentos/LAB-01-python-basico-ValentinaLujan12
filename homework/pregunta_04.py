def pregunta_04():
    """
    La columna 3 contiene una fecha en formato `YYYY-MM-DD`. Retorne la
    cantidad de registros por cada mes, tal como se muestra a continuaciÃ³n.

    Rta/
    [('01', 3),
     ('02', 4),
     ('03', 2),
     ('04', 4),
     ('05', 3),
     ('06', 3),
     ('07', 5),
     ('08', 6),
     ('09', 3),
     ('10', 2),
     ('11', 2),
     ('12', 3)]

    """
    with open("files/input/data.csv", "r") as archivo:
        fechas = [linea.strip().split("\t")[2] for linea in archivo]

    registros = {}

    for fecha in fechas:
        partes = fecha.split("-")
        if len(partes) >= 2:
            mes = partes[1]
            if mes not in registros:
                registros[mes] = 1
            else:
                registros[mes] += 1

    registros = sorted(list(registros.items()))

    return registros